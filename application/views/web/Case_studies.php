              <h3 style="text-align: left;">The ‘time-bomb’ approach used by fraudsters- Part I</h3>             
                <p>As compared with most other wrongdoings like theft, burglary, dacoity, and pilferage, fraud is committed by wrongdoers who are intelligent. Fraud by such intelligent wrongdoers is not a simple act of deception and seizure of opportunity, but an act (or omission) which is executed with great planning and with full anticipation and preparation to face the after effects and consequences to follow. Such fraudsters take adequate steps to ensure that their acts or omissions are camouflaged to prevent detection. Among the many such methods and techniques used by fraudsters, the time bomb technique is the  most devastating. This article illustrates, in two parts, this method using illustrations in two different situations: one in a hospital by a shrewd surgeon to get rid of his competition and one in a five star hotel by an intelligent systems administrator to suppress  hotel  collections.</p>
